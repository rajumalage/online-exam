package com.lara.oes.entity;

public class QuestionAnswer
{
	private int id;
	private String quesId;
	private String opesDesc;
	public int getId()
	{
		return id;
	}
	public void setId(int id)
	{
		this.id = id;
	}
	public String getQuesId()
	{
		return quesId;
	}
	public void setQuesId(String quesId)
	{
		this.quesId = quesId;
	}
	public String getOpesDesc()
	{
		return opesDesc;
	}
	public void setOpesDesc(String opesDesc)
	{
		this.opesDesc = opesDesc;
	}
}
