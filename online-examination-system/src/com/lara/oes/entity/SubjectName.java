package com.lara.oes.entity;

public class SubjectName {
	private int id;
	private String subName;
	private String subDese;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getSubName() {
		return subName;
	}
	public void setSubName(String subName) {
		this.subName = subName;
	}
	public String getSubDese() {
		return subDese;
	}
	public void setSubDese(String subDese) {
		this.subDese = subDese;
	}
	
	
}
